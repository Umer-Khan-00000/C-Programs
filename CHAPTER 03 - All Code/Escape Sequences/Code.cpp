
#include<iostream>
using namespace std;
int main() {
    // Code for creating a new line 
    cout << "\n"; // Creates a new line

    // Code for creating a space in our program
    cout << "\t"; // Causes a space

    // Creates a backspace (where our cursor is) in our program
    cout << "Classs\b-X"; // Causes a backspace 

    // Makes a beep sound in our program
    cout << "\a"; // Causes a beep in the computer 
    
    return 0;
}

